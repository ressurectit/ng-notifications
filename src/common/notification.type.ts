
/**
 * Available types of notifications 
 */
export enum NotificationType
{
    /**
     * Notification informing about success
     */
    success = 0,
    
    /**
     * Notification informing about info
     */
    info = 1,
    
    /**
     * Notification informing about warning
     */
    warning = 2,
    
    /**
     * Notification informing about danger
     */
    danger = 3
}